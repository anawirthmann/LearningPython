print("Digite o numero e direi o dia da semana:") 
numero = int(input())

if numero == 1:
    print("DOMINGO")
elif numero == 2:
    print("SEGUNDA-FEIRA")   
elif numero == 3:
    print("TERÇA-FEIRA")
elif numero == 4:
    print("QUARTA-FEIRA")
elif numero == 5:
    print("QUINTA-FEIRA")
elif numero == 6:
    print("SEXTA-FEIRA")
elif numero == 7:
    print("SÁBADO")

else:
    print("Numero invalido")






